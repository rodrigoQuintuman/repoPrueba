    /**
     * Metodo que permite habilita el boton registrar siempre y cuando los
     * campos contengan informacion y que los JCombo esten con una pcion valida
     */
    public void habilitarBotonRegistrar() {
        boolean estadoJLRut = jLabelMsjRut.isEnabled();
        String nombre = jTextFieldNombres.getText();
        String Paterno = jTextApePaterno.getText();
        String Materno = jTextApeMaterno.getText();
        String Calle = jtfCalle.getText();
        String Numero = jtfNumCalle.getText();

        int telefono = jTextTelefono.getText().length();
        if ((telefono != 9) || Calle.isEmpty() || Numero.isEmpty() || nombre.isEmpty() || Paterno.isEmpty() || Materno.isEmpty() || estadoJLRut || jcbCiudad.getSelectedIndex() == 0 || jcbComuna.getSelectedIndex() == 0) {
            jButtonRegistrar.setEnabled(false);
        } else {
            jButtonRegistrar.setEnabled(true);
        }

    }

