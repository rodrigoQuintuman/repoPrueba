    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed

        Cliente cliente = new Cliente();
        Direccion dire = new Direccion();

        boolean estado;

        //Recuperamos los datos y los asignamos a los Objetos Cliente y Direccion
        cliente.setRut(jTextRut.getText());
        cliente.setDv(jTextFieldDV.getText());
        cliente.setNombre(jTextFieldNombres.getText());
        cliente.setApePaterno(jTextApePaterno.getText());
        cliente.setApeMaterno(jTextApeMaterno.getText());
        cliente.setTelefono(jTextTelefono.getText());

        dire.setNombreCalle(jtfCalle.getText());
        dire.setNumeroCalle(jtfNumCalle.getText());

        //Recuperamos el objeto dentro del Jcombo seleccionado y procedemos a sacar su Id
        Comuna comuna = (Comuna) jcbComuna.getSelectedItem();
        int idCom = comuna.getIdComuna();
        dire.setIdComuna(String.valueOf(idCom));

        estado = op.ingresarCliente(cliente, dire);

        if (estado) {
            JOptionPane.showMessageDialog(null, "Cliente ingresado correctamente");
            limpiarRegistroClientes();
        } else {
            JOptionPane.showMessageDialog(null, "Ups Algo Salio mal y no se pudo registrar el cliente");
        }


    }//GEN-LAST:event_jButtonRegistrarActionPerformed

