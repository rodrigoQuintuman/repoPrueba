    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        String rut = jTextRut.getText();
        String dv = jTextFieldDV.getText();
        Cliente cli = new Cliente();
        cli = op.obtenerCliente(rut, dv);

        if (cli == null) {
            JOptionPane.showMessageDialog(null, "Rut no se encuentra en la BD");
        } else {

            jTextRut.setText("");
            jTextFieldDV.setText("");
            jTextFieldNombres.setText(cli.getNombre());
            jTextApePaterno.setText(cli.getApePaterno());
            jTextApeMaterno.setText(cli.getApeMaterno());
            jTextTelefono.setText(cli.getTelefono());
            System.out.println("dentro de else JButonBuscar antes de rut");
            jTextRut.setText(cli.getRut());
            jTextFieldDV.setText(cli.getDv());
            System.out.println("despues de rut");
            jtfCalle.setText(cli.getNombreCalle());
            jtfNumCalle.setText(cli.getNumeroCalle());
            
            //Validamos el id del Objeto recibido con el id que se almacena en el modelo del Jcombo
            Ciudad ciudadAux = new Ciudad();
            ciudadAux.setIdCiudad(cli.getIdCiudad());
            ComboBoxModel mod = jcbCiudad.getModel();
            for (int i = 0; i < mod.getSize(); i++) {
                Ciudad ciu = (Ciudad) mod.getElementAt(i);
                if (ciu.getIdCiudad() == ciudadAux.getIdCiudad()) {
                    jcbCiudad.setSelectedItem(ciu);//le enviamos al modelo el objeto con el id que coincide
                }
            }
            
            //Validamos el id del Objeto recibido con el id que se almacena en el modelo del Jcombo
            Comuna comunaAux = new Comuna();
            comunaAux.setIdComuna(cli.getIdComuna());
            ComboBoxModel modCom = jcbComuna.getModel();
            for (int i = 0; i < modCom.getSize(); i++) {
                Comuna com = (Comuna) modCom.getElementAt(i);
                if (com.getIdComuna() == comunaAux.getIdComuna()) {
                    jcbComuna.setSelectedItem(com);//le enviamos al modelo el objeto con el id que coincide
                }
            }

            habilitarCampos();
            jButtonModoficar.setEnabled(true);

        }


    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jcbCiudadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbCiudadItemStateChanged
        habilitarBotonModificar();
        Ciudad ciudad = (Ciudad) this.jcbCiudad.getSelectedItem();

        int idCiudad = ciudad.getIdCiudad();
        try {
            llenarCombo.llenarComboComuna(jcbComuna, idCiudad);
        } catch (SQLException ex) {
            Logger.getLogger(ModificarCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcbCiudadItemStateChanged

