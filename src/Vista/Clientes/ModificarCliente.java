    public void habilitarBotonModificar() {

        String nombre = jTextFieldNombres.getText();
        String Paterno = jTextApePaterno.getText();
        String Materno = jTextApeMaterno.getText();
        String Calle = jtfCalle.getText();
        String Numero = jtfNumCalle.getText();
        int telefono = jTextTelefono.getText().length();

        if ((telefono != 9) || Calle.isEmpty() || Numero.isEmpty() || nombre.isEmpty() || Paterno.isEmpty() || Materno.isEmpty() || jcbCiudad.getSelectedIndex() == 0 || jcbComuna.getSelectedIndex() == 0) {
            jButtonModoficar.setEnabled(false);
        } else {
            jButtonModoficar.setEnabled(true);
        }

    }

