    private void jButtonModoficarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModoficarActionPerformed

        int opcion = JOptionPane.showConfirmDialog(null, "¿Seguro que deseas modificarlo? ");

        if (opcion == 0) {
            Cliente cliente = new Cliente();
            Direccion dire = new Direccion();
           //Operaciones op = new Operaciones();
            boolean estado;

            cliente.setRut(jTextRut.getText());
            cliente.setDv(jTextFieldDV.getText());
            cliente.setTelefono(jTextTelefono.getText());

            dire.setNombreCalle(jtfCalle.getText());
            dire.setNumeroCalle(jtfNumCalle.getText());
            Comuna comuna = (Comuna) jcbComuna.getSelectedItem();
            int idCom = comuna.getIdComuna();
            dire.setIdComuna(String.valueOf(idCom));

            estado = op.modificarCliente(cliente, dire);

            if (estado) {
                JOptionPane.showMessageDialog(null, "Cliente modificado correctamente");
                limpiarModificar();
            } else {
                JOptionPane.showMessageDialog(null, "Ups Algo Salio mal y no se pudo modificar el cliente");
            }
        }else if(opcion==2){
            
            limpiarModificar();
        }


    }//GEN-LAST:event_jButtonModoficarActionPerformed

