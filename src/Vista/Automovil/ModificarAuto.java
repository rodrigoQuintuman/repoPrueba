    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed

        String patente = jtfPatenteLetra.getText() + jtfPatenteNumero.getText();
        Automovil auto = new Automovil();
        auto = op.obtenerAuto(patente);

        if (auto == null) {
            JOptionPane.showMessageDialog(null, "Auto no se encuentra en la BD");
            limpiarModificarAuto();
        } else {
            jTextRut.setText(auto.getRutDueno());
            jTextFieldNumMotor.setText(auto.getnMotor());
            jTextFieldKilometraje.setText(auto.getKilometraje());
            
            
            String ano = auto.getAno();
            ComboBoxModel modAno = jcAno.getModel();
            for (int i = 0; i < modAno.getSize(); i++) {
                String anoAux = modAno.getElementAt(i).toString();
                if (anoAux.equalsIgnoreCase(ano)) {
                    jcAno.setSelectedItem(anoAux);
                }
            }
            
            //Validamos el id del Objeto recibido con el id que se almacena en el modelo del Jcombo
            Marca marca = new Marca();
            marca.setIdMarca(Integer.parseInt(auto.getIdMarca()));
            ComboBoxModel modMa = jcMarca.getModel();
            for (int i = 0; i < modMa.getSize(); i++) {
                Marca maAux = (Marca) modMa.getElementAt(i);
                if (maAux.getIdMarca() == marca.getIdMarca()) {
                    jcMarca.setSelectedItem(maAux);//le enviamos al modelo el objeto con el id que coincide
                }
            }
            
            //Validamos el id del Objeto recibido con el id que se almacena en el modelo del Jcombo
            Modelo modelo = new Modelo();
            modelo.setIdModelo(Integer.parseInt(auto.getIdModelo()));
            ComboBoxModel modMod = jcModelo.getModel();
            for (int i = 0; i < modMod.getSize(); i++) {
                Modelo moAux = (Modelo) modMod.getElementAt(i);
                if (moAux.getIdModelo() == modelo.getIdModelo()) {
                    jcModelo.setSelectedItem(moAux);//le enviamos al modelo el objeto con el id que coincide
                }
            }
            
            //Validamos el id del Objeto recibido con el id que se almacena en el modelo del Jcombo
            Color color = new Color();
            color.setIdColor(Integer.parseInt(auto.getIdColor()));
            ComboBoxModel modCol = jcColor.getModel();
            for (int i = 0; i < modCol.getSize(); i++) {
                Color coAux = (Color) modCol.getElementAt(i);
                if (coAux.getIdColor() == color.getIdColor()) {
                    jcColor.setSelectedItem(coAux);//le enviamos al modelo el objeto con el id que coincide
                }
            }

            habilitarCampos();
            jButtonModificar.setEnabled(true);
            jbBuscar.setEnabled(false);

        }
    }//GEN-LAST:event_jbBuscarActionPerformed

