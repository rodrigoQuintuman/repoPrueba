    public void habilitarBotonModificar() {

        String kilometraje = jTextFieldKilometraje.getText();
        String numMotor = jTextFieldNumMotor.getText();

        if (kilometraje.isEmpty() || numMotor.isEmpty() || jcAno.getSelectedIndex() == 0 || jcColor.getSelectedIndex() == 0 || jcModelo.getSelectedIndex() == 0 || jcMarca.getSelectedIndex() == 0) {
            jButtonModificar.setEnabled(false);
        } else {
            jButtonModificar.setEnabled(true);
        }

    }

    public void habilitarBotonBuscar() {
        int PatenteLetra = jtfPatenteLetra.getText().length();
        int PatenteNumero = jtfPatenteNumero.getText().length();
        if (PatenteLetra != 4 || PatenteNumero != 2) {
            jbBuscar.setEnabled(false);
        } else {
            jbBuscar.setEnabled(true);
        }

    }

