    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        int opcion = JOptionPane.showConfirmDialog(null, "¿Seguro que deseas modificarlo? ");

        if (opcion == 0) {
            Automovil auto = new Automovil();

            boolean estado;

            auto.setRutDueno(jTextRut.getText());
            auto.setKilometraje(jTextFieldKilometraje.getText());
            auto.setnMotor(jTextFieldNumMotor.getText());
            auto.setPatente(jtfPatenteLetra.getText() + jtfPatenteNumero.getText());

            auto.setAno(jcAno.getModel().getSelectedItem().toString());
            
            //Recuperamos el objeto dentro del Jcombo seleccionado y procedemos a sacar su Id(Modelo y Color)
            Modelo modelo = (Modelo) jcModelo.getSelectedItem();
            int idMod = modelo.getIdModelo();
            auto.setIdModelo(String.valueOf(idMod));

            Color color = (Color) jcColor.getSelectedItem();
            int idCol = color.getIdColor();
            auto.setIdColor(String.valueOf(idCol));

            estado = op.modificarAuto(auto);

            if (estado) {
                JOptionPane.showMessageDialog(null, "Auto modificado correctamente");
                limpiarModificarAuto();
            } else {
                JOptionPane.showMessageDialog(null, "Ups Algo Salio mal y no se pudo modificar los datos del Automovil");

            }
        } else if (opcion == 2) {

            limpiarModificarAuto();
        }

    }//GEN-LAST:event_jButtonModificarActionPerformed

