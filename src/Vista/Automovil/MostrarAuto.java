    public void tamanoTabla() {
        if (jTableAuto.getColumnModel().getColumnCount() > 0) {
            jTableAuto.getColumnModel().getColumn(0).setPreferredWidth(25);
            jTableAuto.getColumnModel().getColumn(1).setPreferredWidth(70);
            jTableAuto.getColumnModel().getColumn(2).setPreferredWidth(100);
            jTableAuto.getColumnModel().getColumn(3).setPreferredWidth(50);
            jTableAuto.getColumnModel().getColumn(5).setPreferredWidth(100);
            jTableAuto.getColumnModel().getColumn(6).setPreferredWidth(130);

        }

    }
    
     public void limpiarTabla() {
        for (int i = 0; i < jTableAuto.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }

    }
    
      public void habilitarBotonBuscar() {
        int PatenteLetra = jtPatenteLetra.getText().length();
        int PatenteNumero = jtPatenteNumero.getText().length();
        if (PatenteLetra != 4 || PatenteNumero != 2) {
            jbBuscar.setEnabled(false);
        } else {
            jbBuscar.setEnabled(true);
        }

    }
      
      public void Actualizar() {

        try {
            limpiarTabla();
            op.llenarTablaAuto(modelo);
            jtPatenteLetra.setText("");
            jtPatenteNumero.setText("");
            tamanoTabla();
        } catch (SQLException ex) {
            Logger.getLogger(MostrarAuto.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
