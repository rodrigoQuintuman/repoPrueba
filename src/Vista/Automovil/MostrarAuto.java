    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
       String patenteLetra = jtPatenteLetra.getText();
        String patenteNumero = jtPatenteNumero.getText();
        try {
            modelo = new DefaultTableModel(null, titulo);
            jTableAuto.setModel(modelo);
            op.buscarAuto(modelo,patenteLetra+patenteNumero);
            tamanoTabla();
            if (0==modelo.getRowCount()) {
                JOptionPane.showMessageDialog(null, "Auto no se encuentra en la BD");
                Actualizar();
                
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(MostrarAuto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        int aux = jTableAuto.getSelectedRow();
        boolean estado;
        if (aux == -1) {

            JOptionPane.showMessageDialog(null, "Seleccione un auto a eliminar");

        } else {

            int opcion = JOptionPane.showConfirmDialog(null, "¿Seguro que deseas eliminarlo? ");

            if (opcion == 0) {

                String patente = (String) jTableAuto.getValueAt(aux, 1);
                estado = op.eliminarAuto(patente);
                if (!estado) {
                    JOptionPane.showMessageDialog(null, "Auto no pudo ser  eliminado");
                    Actualizar();
                }else{
                JOptionPane.showMessageDialog(null, "Auto eliminado con exito");
                Actualizar();
                }
            }   
        }
    }//GEN-LAST:event_jbEliminarActionPerformed


