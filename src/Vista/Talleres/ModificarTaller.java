    private void jbModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbModificarActionPerformed
       
         int opcion = JOptionPane.showConfirmDialog(null, "¿Seguro que deseas modificarlo? ");

        if (opcion == 0) {
            Taller taller = new Taller();
            Direccion dire = new Direccion();
          
            boolean estado;

            taller.setNombreSucursal(jtfNombre.getText());
            taller.setEmail(jtfCorreo.getText());
            taller.setTelefono(jtfTelefono.getText());

            dire.setNombreCalle(jtfCalle.getText());
            dire.setNumeroCalle(jtfNumeroCalle.getText());
            Comuna comuna = (Comuna) jcbComuna.getSelectedItem();
            int idCom = comuna.getIdComuna();
            dire.setIdComuna(String.valueOf(idCom));

            estado = op.modificarTaller(taller, dire);

            if (estado) {
                JOptionPane.showMessageDialog(null, "Taller modificado correctamente");
                limpiarModificar();
            } else {
                JOptionPane.showMessageDialog(null, "Ups Algo Salio mal y no se pudo modificar el Talller");
            }
        }else if(opcion==2){
            
            limpiarModificar();
        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_jbModificarActionPerformed

