     public void habilitarBotonModificar() {
         
        boolean correo = Validar.validarCorreo(jtfCorreo, jlMsgEmail);
        String nombre = jtfNombre.getText();
        String calle = jtfCalle.getText();
        String numeroCalle = jtfNumeroCalle.getText();
        int telefono = jtfTelefono.getText().length();

        if ((telefono != 9) || calle.isEmpty() || numeroCalle.isEmpty() || nombre.isEmpty() || correo || jcbCiudad.getSelectedIndex() == 0 || jcbComuna.getSelectedIndex() == 0) {
            jbModificar.setEnabled(false);
        } else {
            jbModificar.setEnabled(true);
        }

    }
    
