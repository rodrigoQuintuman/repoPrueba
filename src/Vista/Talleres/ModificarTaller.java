    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed

        String nombre = jtfNombre.getText();
        Taller taller = new Taller();
        taller = op.obtenerTaller(nombre);

        if (taller == null) {
            JOptionPane.showMessageDialog(null, "Nombre de Sucursal no se encuentra en la BD");
        } else {

            jtfNombre.setText(taller.getNombreSucursal());
            jtfCorreo.setText(taller.getEmail());
            jtfCalle.setText(taller.getNombreCalle());
            jtfNumeroCalle.setText(taller.getNumeroCalle());
            jtfTelefono.setText(taller.getTelefono());
           
            Ciudad ciudadAux = new Ciudad();
            ciudadAux.setIdCiudad(taller.getIdCiudad());
            ComboBoxModel mod = jcbCiudad.getModel();
            for (int i = 0; i < mod.getSize(); i++) {
                Ciudad ciu = (Ciudad) mod.getElementAt(i);
                if (ciu.getIdCiudad() == ciudadAux.getIdCiudad()) {
                    jcbCiudad.setSelectedItem(ciu);
                }
            }

            Comuna comunaAux = new Comuna();
            comunaAux.setIdComuna(taller.getIdComuna());
            ComboBoxModel modCom = jcbComuna.getModel();
            for (int i = 0; i < modCom.getSize(); i++) {
                Comuna com = (Comuna) modCom.getElementAt(i);
                if (com.getIdComuna() == comunaAux.getIdComuna()) {
                    jcbComuna.setSelectedItem(com);
                }
            }

            habilitarCampos();
            jbModificar.setEnabled(true);

        }

    }//GEN-LAST:event_jbBuscarActionPerformed

