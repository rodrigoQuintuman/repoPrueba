    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        Taller taller = new Taller();
        Direccion dire = new Direccion();

        boolean estado;

        //Recuperamos los datos y los asignamos a los Objetos Taller y Direccion
        taller.setNombreSucursal(jtfNombre.getText());
        taller.setEmail(jtfCorreo.getText());
        taller.setTelefono(jtfTelefono.getText());

        dire.setNombreCalle(jtfCalle.getText());
        dire.setNumeroCalle(jtfNumeroCalle.getText());
        Comuna comuna = (Comuna) jcbComuna.getSelectedItem();
        int idCom = comuna.getIdComuna();
        dire.setIdComuna(String.valueOf(idCom));

        estado = op.ingresarTaller(taller, dire);

        if (estado) {
            JOptionPane.showMessageDialog(null, "Taller ingresado correctamente");
            limpiarRegistrarTaller();
        } else {
            JOptionPane.showMessageDialog(null, "Ups Algo Salio mal y no se pudo registrar el Taller");
        }


    }//GEN-LAST:event_jButtonRegistrarActionPerformed


