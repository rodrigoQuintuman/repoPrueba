    private void jcbCiudadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbCiudadItemStateChanged
        habilitarBotonRegistrar();
        Ciudad ciudad = (Ciudad) this.jcbCiudad.getSelectedItem();//recuperamos el objeto (Ciudad) que esta en jComboBox, segun el Item Seleccionda
        int idCiu = ciudad.getIdCiudad();//Recuperamos el Id del Objeto Ciudad 
        try {
            llenarCombo.llenarComboComuna(jcbComuna, idCiu);//enviamos el JComboBoxComuna y el Id de la de la Ciudad 
        } catch (SQLException ex) {
            Logger.getLogger(RegistrarTaller.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jcbCiudadItemStateChanged

