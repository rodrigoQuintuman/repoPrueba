    /**
     * Metodo que permite habilita el boton registrar siempre y cuando los
     * campos contengan informacion y que los JCombo esten con una pcion valida
     */
    public void habilitarBotonRegistrar() {

        boolean correo = Validar.validarCorreo(jtfCorreo, jlMsgEmail);

        String nombre = jtfNombre.getText();
        String calle = jtfCalle.getText();
        String numeroCalle = jtfNumeroCalle.getText();
        int largoTelefono = jtfTelefono.getText().length();

        if ((largoTelefono != 9) || nombre.isEmpty() || calle.isEmpty() || numeroCalle.isEmpty() || !correo || jcbCiudad.getSelectedIndex() == 0 || jcbComuna.getSelectedIndex() == 0) {
            jButtonRegistrar.setEnabled(false);
        } else {
            jButtonRegistrar.setEnabled(true);
        }

    }

    /**
     * Metodo que permite limpiar los campos despues de una accion
     */
    public void limpiarRegistrarTaller() {

        jtfNombre.setText("");
        jtfCalle.setText("");
        jtfCorreo.setText("");
        jtfTelefono.setText("");
        jtfNumeroCalle.setText("");
        jcbCiudad.setSelectedIndex(0);
        jcbComuna.setSelectedIndex(0);
        jlMsgEmail.setText("");

    }

